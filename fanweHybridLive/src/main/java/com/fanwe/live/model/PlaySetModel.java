public class PlaySetModel
{

    private String url;
    private String definition;
    private String vbitrate;
    private String vheight;
    private String vwidth;

    public String getUrl()
    {
        return url;
    }

    public void setUrl(String url)
    {
        this.url = url;
    }

    public String getDefinition()
    {
        return definition;
    }

    public void setDefinition(String definition)
    {
        this.definition = definition;
    }

    public String getVbitrate()
    {
        return vbitrate;
    }

    public void setVbitrate(String vbitrate)
    {
        this.vbitrate = vbitrate;
    }

    public String getVheight()
    {
        return vheight;
    }

    public void setVheight(String vheight)
    {
        this.vheight = vheight;
    }

    public String getVwidth()
    {
        return vwidth;
    }

    public void setVwidth(String vwidth)
    {
        this.vwidth = vwidth;
    }
}
